    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
        // TODO add your handling code here:
        itemcost[0] = Double.parseDouble(jtxtDosa.getText()) * 150 ;
        itemcost[1] = Double.parseDouble(jtxtDosa1.getText()) * 100 ;
        itemcost[2] = Double.parseDouble(jtxtDosa2.getText()) * 180 ;
        itemcost[3] = Double.parseDouble(jtxtDosa3.getText()) * 200 ;
        itemcost[4] = Double.parseDouble(jtxtPunjabi.getText()) * 180 ;
        itemcost[5] = Double.parseDouble(jtxtPunjabi1.getText()) * 160 ;
        itemcost[6] = Double.parseDouble(jtxtPunjabi2.getText()) * 180 ;
        itemcost[7] = Double.parseDouble(jtxtPunjabi3.getText()) * 190 ;
        itemcost[8] = Double.parseDouble(jtxtPunjabi4.getText()) * 30 ;
        itemcost[9] = Double.parseDouble(jtxtPunjabi5.getText()) * 45 ;
        itemcost[10] = Double.parseDouble(jtxtChinese.getText()) * 75 ;
        itemcost[11] = Double.parseDouble(jtxtChinese1.getText()) * 90 ;
        itemcost[12] = Double.parseDouble(jtxtChinese2.getText()) * 110 ;
        itemcost[13] = Double.parseDouble(jtxtPizza.getText()) * 185 ;
        itemcost[14] = Double.parseDouble(jtxtPizza1.getText()) * 195 ;
        itemcost[15] = Double.parseDouble(jtxtPizza2.getText()) * 225 ;
        itemcost[16] = Double.parseDouble(jtxtPizza3.getText()) * 250 ;
        itemcost[17] = Double.parseDouble(jtxtPizza4.getText()) * 220 ;
        itemcost[18] = Double.parseDouble(jtxtPizza5.getText()) * 210 ;
        itemcost[19] = Double.parseDouble(jtxtPizza6.getText()) * 45 ;
        itemcost[20] = Double.parseDouble(jtxtFast.getText()) * 100 ;
        itemcost[21] = Double.parseDouble(jtxtFast1.getText()) * 30 ;
        itemcost[22] = Double.parseDouble(jtxtD.getText()) * 15 ;
        itemcost[23] = Double.parseDouble(jtxtD1.getText()) * 10 ;
        itemcost[24] = 0;
        
        for(int i = 0 ; i<24 ; i++)
        {
            itemcost[24] = itemcost[24] + itemcost[i];
        }
       
        iTax = String.format("Rs  %.2f",itemcost[24]*0.1 );
        iSubTotal = String.format("Rs  %.2f",itemcost[24] );
        iTotal = String.format("Rs  %.2f",itemcost[24] + itemcost[24]*0.1);
        
        jtxtTax.setText(iTax);
        jtxtSub.setText(iSubTotal);
        jtxtTotal.setText(iTotal);
        
        jtxtReceipt.setEnabled(true);
        int ref = 1234 + (int) (Math.random()*5678);
        Calendar timer = Calendar.getInstance();
        timer.getTime();
        SimpleDateFormat tTime = new SimpleDateFormat("HH:mm:ss");
        tTime.format(timer.getTime());
        SimpleDateFormat Tdate = new SimpleDateFormat("dd-mm-yyy");
        Tdate.format(timer.getTime());
       
      jtxtReceipt.setText("Ref :"+ref+"\n================================\n"+" Thanks for food order");
      
    }//GEN-LAST:event_jbtnTotalActionPerformed

